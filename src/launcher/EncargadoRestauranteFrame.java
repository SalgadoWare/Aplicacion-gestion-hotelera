    private void bDarAltaAlimentosBebidasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDarAltaAlimentosBebidasActionPerformed
        try{
            //Lanzamos el di√°logo
            if(darAltaAlimentosYBebidasDialog.showDialog()){
                //Si se ha pulsado aceptar tenemos que guardar toda la info en la BD
            	String nombre = darAltaAlimentosYBebidasDialog.getNombre();
                String tipo = darAltaAlimentosYBebidasDialog.getTipo();
                Map<String,Double> lugares_precios = new HashMap<>();
                LocalDate fechaAlta = darAltaAlimentosYBebidasDialog.getFechaAlta();
                int diaAlta = fechaAlta.getDayOfMonth();
                int mesAlta = fechaAlta.getMonthValue();
                int anioAlta = fechaAlta.getYear();
                
                if(darAltaAlimentosYBebidasDialog.isLugarRestaurante())
                    lugares_precios.put("restaurante", darAltaAlimentosYBebidasDialog.getPrecioRestaurante());
                if(darAltaAlimentosYBebidasDialog.isLugarCafeteria())
                    lugares_precios.put("cafeteria", darAltaAlimentosYBebidasDialog.getPrecioCafeteria());
                if(darAltaAlimentosYBebidasDialog.isLugarMinibar())
                    lugares_precios.put("minibar", darAltaAlimentosYBebidasDialog.getPrecioMinibar());
                
                DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            	
                //Traza
                System.out.println("\nAlimento:");
                System.out.println("Nombre: "+nombre);
                System.out.println("Tipo: "+tipo);
                System.out.println("Fecha Alta: "+dtf.format(fechaAlta));
                for(Map.Entry<String,Double> par : lugares_precios.entrySet()){
                    System.out.println("Lugar: "+par.getKey()+" Precio: "+par.getValue());
                }
                
                //Guardamos la info en la BD (hay que restarle 1 al mes porque los meses en java.sql empiezan en 0
    		boolean creado = BD.crearAlimento(nombre, tipo, new java.sql.Date(anioAlta,mesAlta-1,diaAlta), lugares_precios);
                if(creado)
                    System.out.println("Se ha creado el alimento");
                else System.out.println("Error creando el alimento");
            }
        } catch(Exception e){e.printStackTrace();}
        
    }//GEN-LAST:event_bDarAltaAlimentosBebidasActionPerformed

